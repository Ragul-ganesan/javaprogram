package java_demo_prc;

public class Constructor_Overloading1 {
	public Constructor_Overloading1 () {
		System.out.println("hi Ragul");
		
	}
	public  Constructor_Overloading1(int A,double B,String D) {
		System.out.println(A);
		System.out.println(B);
		System.out.println(D);
	}
	public  Constructor_Overloading1(double C,int D,String E) {
		System.out.println(C);
		System.out.println(D);
		System.out.println(E);
	}
	
	

}
